public class CompareException extends Exception{



	/**
	 * 
	 */
	private static final long serialVersionUID = 6986408493039966907L;

	public CompareException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public CompareException(String arg0) {
		super(arg0);
		
	}

	public CompareException(Throwable arg0) {
		super(arg0);
		
	}
	
	



}
